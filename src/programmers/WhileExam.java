package programmers;

public class WhileExam {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {
            i++;
            System.out.println(i);
        }

        int j = 0;
        while (j <= 10) {
            System.out.println(j);
            j++;
        }
    }
}
